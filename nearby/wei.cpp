int n;
int a[N];
int mx = -inf, mx2 = -inf;
int mn = inf, mn2 = inf;
bool pos[N];
bool umx = false, umn = false;

void logic() {
    ms(pos, true);
    cin >> n;
    for (int i = 1; i <= n; ++i) {
        cin >> a[i];
        mx = max(mx, a[i]);
        mn = min(mn, a[i]);
    }
    for (int i = 1; i <= n; ++i) {
        if (!umx and a[i] == mx) {
            pos[i] = false;
            umx = true;
        }
        else if (!umn and a[i] == mn) {
            pos[i] = false;
            umn = true;
        }
    }
    for (int i = 1; i <= n; ++i) {
        if (pos[i]) {
            mx2 = max(mx2, a[i]);
            mn2 = min(mn2, a[i]);
        }
    }

    cout << max(abs(mx + mx2), abs(mn + mn2));
    // execute;
}

