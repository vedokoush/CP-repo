int add(int a, int b) {return (a + b) % mod;}
int mul(int a, int b) {return (a * b) % mod;}
int sub(int a, int b) {return ((a - b) % mod + mod) % mod;}

int n, k;
int dp[N][M];

void logic() {
    cin >> n >> k;
    int sum = n * (n + 1) / 2;
    if (sum < k || (sum - k) % 2 != 0) {
        cout << 0;
        return;
    }
    int t = (sum - k) / 2;

    dp[0][0] = 1;

    for (int i = 1; i <= n; i++) {
        for (int j = 0; j <= t; j++) {
            dp[i][j] = dp[i - 1][j];

            if (j >= i) {
                dp[i][j] = add(dp[i][j], dp[i - 1][j - i]);
            }
        }
    }

    int res = dp[n][t];
    if (k == 0) {
        if (res % 2 == 0) {
            res = res / 2;
        } else {
            res = (res + mod) / 2;
        }
    }
cout << res;
    // execute;
}

